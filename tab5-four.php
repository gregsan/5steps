<h2>Четвертый урок<br>
Цена - это всего лишь размер желания</h2>
<p class="item-intro text-muted"></p>
<p align="justify">
- Торговля совсем встала.<br>
- Как увидел?<br>
- Смотрю на предложения по вторичке авто.<br>
- И что?<br>
- Одни и те же предложения. Качество машин низкое, а цены поднимают Видимо денег у людей нет.<br>
- Деньги у людей есть всегда.<br>
- Как же есть, если все вокруг воют, что стало плохо?<br>
- Новые тачки в городе есть?<br>
- Есть.<br>
- Заправляются?<br>
- Да.<br>
- На охоту и рыбалку?<br>
- Каждый выходной!<br>
- Деньги есть.<br>
- В чем же дело?<br>
- Причина проста, но совсем не очевидна.<br>
- У Вас всегда так. Найдете причину в самом неожиданном месте. Рассказывайте!<br>
- Чтобы понять давай разберемся как выставляется цена.<br>
- Чего тут сложного? Поставили какую захотели!<br>
- Не совсем так. На любой товар, всегда есть 2 системы ориентиров.<br>
- Аж две?<br>
- Да. Первая – это «товары сравнения». Любой товар, в нашей голове, зажат между «чуть похуже и дешевле» и «чуть получше и дороже». Причем, не всегда это похожие товары. Не всегда это товары одного рынка. А иногда, в качестве товара выступают «не совсем товары».<br>
- Не понял. <br>
- Например, Я собираюсь выставить цену на квартиру или собираюсь оценить стоит ли купить квартиру. Например в Черкассах. Я могу сравнить с квартирой в другом районе города. Или с домом. Или с квартирой в другой стране. Или с динамикой курса доллара. Или с депозитом. Или с доходность моих товаров.<br>
- Так понятнее. А почему три, а не четыре?<br>
- Потому что три – это минимальный интервал границ. От и до, а текущий товар между. И вот мы будем сравнивать насколько цена справедлива, по тому, в каком коридоре эта цена.<br>
- Это первая? А вторая система?<br>
- Вторая сложнее немножко. Каждая цена любого товара находится в «интервале сделок».<br>
- Первое было «интервал сравнения», а это что такое?<br>
- Это промежуток между «платежеспособностью» и «смыслом продажи». Допустим ты решил продать свою машину. Если поставишь высокую цену, то у желающих может не быть столько денег. Желание есть, а способности нет.<br>
- А смысл продажи?<br>
- У тебя всегда есть низ цены, типа «за такие деньги пусть хоть пропадет» или «проще подарить, чем продать».<br>
- Есть такое. Точно! Именно в этом коридоре будут совершаться сделки. Что дальше?<br>
- А дальше … Подходим к причине. И первая система и вторая работают на основе «известной тебе информации».<br>
- И что тут сложного? Открыл газету и посмотрел.<br>
- Ты видишь цены предложений, а не сделок.<br>
- А в чем отличие?<br>
- Ты не обладаешь информацией о реальных сделках, реальных ценах, реальном количестве и скорости совершения. Представь себе, что ты едешь на машине, у которой не работают приборы, а в окнах приклеена разные фотографии. Будешь на газ давить?<br>
- Нет конечно.<br>
- А если надо ехать?<br>
- Осторожненько буду.<br>
- Но ведь ничего не угрожает! Ничего опасного нет! Это всего лишь твой недостаток информации! Точнее, принятие решений на основе доступной информации. Теперь представь, что все видят предложения и не знают цен реальных сделок. И эти предложения, являются друг для друга «интервалом сравнения».<br>
- Ничего сложного. Так и есть.<br>
- А теперь представь, что мы резко понизим всю платежеспособность всех рынков. Сожмем денежную массу.<br>
- Покупать перестанут.<br>
- Дальше!<br>
- Перестанут … Денег в обороте станет меньше.<br>
- Дальше!<br>
- Не понимаю.<br>
- Представь себе, что ты же не знаешь про платежеспособность. Ждал, ждл, разочаровался и потерял смысл продавать. Что сделаешь?<br>
- Уберу предложение.<br>
- Что подумают остальные?<br>
- Блин! Что я продал!<br>
- Изменят цену?<br>
- Нет конечно. Ведь кто-то продал.<br>
- Не просто кто-то, а товар сравнения. Например у тебя бы «Форд» старый, а у меня старый «Жигуль». Я же буду считать, что раз ты продал, то у мен есть шанс!<br>
- В точку! Так и есть.<br>
- Подождешь, но платежеспособность?<br>
- Она еще меньше, потому что ниже оборачиваемость.<br>
- Что надо?<br>
- Всем понизить цены, чтобы попасть в интервал …<br>
- Сделок<br>
- Да! Так почему они этого не делают?<br>
- Сложно. Ты вот тоже решения не видел.<br>
- В чём же дело? Надо всем рассказать!<br>
- Ничего не изменится.<br>
- А что поможет?<br>
- Доступная информация о реальных сделках.<br>
- Кто же расскажет? Стоп! А как на Западе?<br>
- Там есть биржа и такое понятие как «публичная компания».Хочешь продавать свои доли – рассказывай всем все честно.<br>
- И всего-то? Такая малость?<br>
- Это не малость. Это изменить все законодательство! И налоговое, и бюджетное, и пенсии, и формирование государственных платежей. И еще много чего. Причем одним махом.<br>
- Почему Вы им не расскажите?<br>
- Они не услышат. Большинство из них считают бизнесменов мошенниками и прохиндеями.<br>
- Задача не разрешима?<br>
- Решение есть всегда. То, что нельзя сделать в большом, можно сделать в малом. Показав работающую модель.

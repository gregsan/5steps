<p align="justify">
							- А вот то, чему ты учишь, оно для какого сектора рынка?<br>
- Для любого<br>
- Такого не может быть<br>
- Наверное<br>
- Ну, есть розничная торговля, оптовая, битуби, битуси … Там же всё по-разному<br>
- Принципы везде одинаковые<br>
- Почем их никто не описал? Где взял их ты?<br>
- Я не знаю, описал ли их кто. Мне пришлось их понять.<br>
- Кто-то заставил?<br>
- Жизнь. Когда я входил на рекламный рынок, то там уже были очень устойчивые и сильные игроки. Мне оставалась участь гаражника, подбирающего крошки со стола заказов рынка.<br>
- Тебя это не устраивало?<br>
- У меня гаража не было. У меня был офис в самом центре города. Центрее мэрии.<br>
- Дорогая аренд обязывала?<br>
- Нет. Мне повезло. я мог несколько месяцев не платить аренду. У моего компаньона был хороший торговый бизнес и он оплачивал всю аренду. Я имел небольшую подушку безопасности.<br>
- Значит не голод. Место обязывало.<br>
- Да. Так получилось. Либо я становился центральным агентством, либо улетал с рынка. Сначала я попытался делать всё как конкуренты. Пытался креативить и делать дорого и «красиво». Мне на это не хватало ни опыта, ни ресурсов. И тогда я предложил рынку рискованную схему.<br>
- Какую?<br>
- Предложил платить мне от разницы результатов. До рекламы и после.<br>
- Не понял? Как замерять-то?<br>
- Сначала я просто предложил. Даже помню как это получилось. Не было никакого заранее созданного плана или стратегии. Просто в город вошел очень крупный торговец канцтоварами и во время переговоров я решил его удивить чем-то нестандартным. Ну и брякнул.<br>
- Блеф?<br>
- Нет. Именно канцтовары я знал неплохо. Я своего товарища на рынок хорошо вывел. Это был мой самый первый крупный клиент. Я с ним прошел начиная от выбора темы. Тогда мы анализировали все темы. Начиная от продуктов и до развлечений. И остановились на бумаге и канцтоварах. Так что эту тему я знал.<br>
- И что дальше?<br>
- Дальше … они офигели от наглости. И согласились. <br>
- На что?<br>
- Начать. Предложил им самим сказать как они будут считать результаты от рекламы. Оказалось, что методов не было. Ну, они как-то определяли, что реклама работает. Как-то замеряли продажи. Но сказать точно, от рекламы был результат или от работы персонала было сложно.<br>
- И?<br>
- Дальше мы начали придумывать вместе. Сначала к продажам привязались. Оказалось, что я, как рекламщик не могу влиять на продажи. Потом привязались к посещениям. Оказалось, что им не выгодно нагонять просто любопытных. На это у нас ушло довольно много времени. К моему счастью, они оплатили эти эксперименты. Это были очень хорошие ребята. Без снобизма и с большим желанием дойти до, предложенной мной, схемы.<br>
- А тебе это зачем надо было?<br>
- В случае успеха, я бы имел уникальное предложение, по отношению к конкурентам.<br>
- Решил?<br>
- Да. В один момент мне в голову пришло изящное решение. Мне требовалось связать рекламу и продажи. Причём так, чтобы мы вместе с заказчиком были заинтересованы потратить меньше денег. Потому что если продажи вверх за счет расходов вверх, то это не есть гуд.<br>
- Им пришлось взять тебя на работу?<br>
- Это тоже невозможно. Короче, я предложил им вывести с каждой товарной позиции процент на рекламу. Ну, сколько они готовы направлять на рекламу от каждой продажи.<br>
- И отдавать тебе?<br>
- Нет. Потому что у них не будет уверенности, что это будет работать. Я предложил им просто создать рекламный фонд от продаж этого месяца. И озвучить мне сумму. Узнав сумму, я продумывал рекламные мероприятия. И после их оплаты, мы остаток от рекламного фонда делили пополам. Так они получали экономию, и я был заинтересован в том, чтобы делать дешевле, но так, чтобы это давало рост рекламного фонда. А для этого должны расти продажи.<br>
- Смело. И справился?<br>
- Да. Мне пришлось напрячься. Пришлось начать много смотреть по сторонам, анализировать. Получилось так, что в борьбе за ресурс я стал сильнее. И ко мне пошли другие заказчики.<br>
- А что конкуренты?<br>
- Они заметили не скоро. Ко мне пошли те, кто для них был плохим клиентом. Им они не могли дать больших бюджетов и выклёвывали мозг. Они освободились от этого и были рады.<br>
- А ты получил этот геморрой?<br>
- Я получил крепких заказчиков, с разных секторов рынка, с реальными деньгами. Это были жилы и мышцы рынка. И мне пришлось разобраться со всеми секторами. Со всеми продажами. В борьбе за свои деньги. Мне пришлось найти общие принципы, чтобы меня не порвали разные клиенты.<br>
- А потом?<br>
- А потом был 98-ой. И жирных клиентов не стало. Остались только жилистые. А с ними я умел работать лучше всех и забрал рынок.<br>
- Ты разбогател?<br>
- Нет. Я был не готов. И захлебнулся.<br>
- А как не захлебнуться?<br>
- Вот этому я и учу людей. Как не делать моих ошибок.<br>
- Зачем тебе это?<br>
- Мне приятно видеть как они становятся сильнее.</p>
